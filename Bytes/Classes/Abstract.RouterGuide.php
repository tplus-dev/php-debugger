	namespace Bytes;

	abstract class RouterGuide {

	    /* ======================================================================================================
	       PROPERTIES
	    ====================================================================================================== */

	    /**
	     * Pattern to be compared against
	     * @var string
	     */
	    
	    protected $Pattern;

	    /* ======================================================================================================
	       ABSTRACT METHODS
	    ====================================================================================================== */

	    /* ------------------------------------------------------------------------------------------------------
	       MATCH
	    ------------------------------------------------------------------------------------------------------ */

	    /**
	     * Match against input
	     *
	     * @abstract
	     * @access public
	     * @param string $Input
	     * @return bool
	     */
	    
	    abstract public function Match ( string $Input ): bool;

	    /* ======================================================================================================
	       CONSTRUCTOR
	    ====================================================================================================== */

	    /**
	     * Class constructor
	     *
	     * @access public
	     * @param string $Pattern
	     * @return void
	     */
	    
	    public function __construct ( string $Pattern ) {

	        /* ------------------------------------------------------------------------------------------------------
	           SET
	        ------------------------------------------------------------------------------------------------------ */

	        $this -> Pattern 		= $Pattern;

	    }

	}